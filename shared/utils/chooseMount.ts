export const chooseMouth = (mounth: string) => {
  switch (mounth) {
    case '01':
      return 'Января';
      break;
    case '02':
      return 'Февраля';
      break;
    case '03':
      return 'Марта';
      break;
    case '04':
      return 'Апреля';
      break;
    case '05':
      return 'Мая';
      break;
    case '06':
      return 'Июнь';
      break;
    case '07':
      return 'Июль';
      break;
    case '08':
      return 'Август';
      break;
    case '09':
      return 'Сентябрь';
      break;
    case '10':
      return 'Октябрь';
      break;
    case '11':
      return 'Ноябрь';
      break;
    case '12':
      return 'Декабрь';
      break;
    default:
      break;
  }
};
